//Задача 11. преобразовать числовой массив в булево
public class Task_10 {
    public static void main(String[] args) {
        int value = 1;

        boolean boolValue;

        if (value >= 1) {
            boolValue = true;
        } else {
            boolValue = false;
        }


        System.out.println(value + " after converting into boolean = " + boolValue);
    }
}


